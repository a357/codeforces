package com.prituladima.geeksforgeeks.geometric_algorithms.lines.klee;

public class LSegment {

    private int first;
    private int second;

    public LSegment(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}